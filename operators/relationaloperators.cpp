// Relational operators:
// <, >, <=, >=, ==, !=

#include <iostream>
using namespace std;
void main () 
{
    int a = 5, b = 5;
    cout << (a > b) << endl;
    cout << (a < b) << endl;
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;
    cout << (a == b) << endl;
    cout << (a != b) << endl;
}